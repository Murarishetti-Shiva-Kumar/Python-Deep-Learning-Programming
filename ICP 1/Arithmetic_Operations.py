# Take two numbers from user and perform arithmetic operations on them

a = int(input("Enter first number:"))  # Taking the first number as input
b = int(input("Enter second number:"))  # Taking the second number as input

# performing arithmetic operations
print("Addition Operation:", a + b)  # Addition Operation
print("Subtraction Operation:", a - b)  # Subtraction Operation
print("Multiplication Operation:", a * b)  # Multiplication Operation
print("Division Operation:", a / b)  # Division Operation
print("Modulus Operation:", a % b)  # Modulus Operation
print("Exponential calculation:", a ** b)  # Exponential calculation
print("Floor Division:", a // b)  # Floor Division
